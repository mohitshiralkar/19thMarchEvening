package loop;

public class ForLoopStudy {

	public static void main(String[] args) {
		
		String Name="Mohit";
		for(int i=1;i<=10;i++)
		{
          System.out.println(Name);
	}
       
		for(int i=2;i<=20;i=i+2)
		{
			System.out.println(i);
		}
		
	    for(int i=40;i>=4;i=i-4)
	    {
	     System.out.println(i);
	    }
}
	
}
      