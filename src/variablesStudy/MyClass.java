package variablesStudy;

public class MyClass {

	public static void main(String[] args) {
		
		//1. Variable declaration (Allocating/Reserving memory)
		String studentName;
		
		//2. Variable Initialization (Assigning or Inserting value)
		studentName = "Mohit";
				
		//3. usage
		System.out.println(studentName);
		
		//1. Variable declaration
		int RollNum;
		
		//2. variable Initialization (assign value)
		RollNum = 76;
		
		//3. usage
		System.out.println(RollNum);
		
		//1. Variable declaration
		char grade;
		
		//2. variable Initialization
		grade = 'A';
		
	    //3. usage
		System.out.println(grade);
		
		
		System.out.println("My name is "+studentName);
		System.out.println("My roll number is "+RollNum);
		System.out.println("My grade is "+grade);
		
		
		
		}

}


            






